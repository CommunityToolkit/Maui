namespace CommunityToolkit.Maui.PlatformConfiguration.AndroidSpecific;

/// <summary>
/// Represents the style of the navigation bar on Android.
/// </summary>
public enum NavigationBarStyle
{
	/// <summary>
	/// The default navigation bar style.
	/// </summary>
	Default = 0,

	/// <summary>
	/// The light content navigation bar style.
	/// </summary>
	LightContent = 1,

	/// <summary>
	/// The dark content navigation bar style.
	/// </summary>
	DarkContent = 2
}