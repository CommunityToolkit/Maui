namespace CommunityToolkit.Maui.Core;

/// <summary>
/// Enum that represents the possible style values for the device
/// </summary>
public enum StatusBarStyle
{
	/// <summary>
	/// The default style value
	/// </summary>
	Default = 0,
	/// <summary>
	/// Light style value
	/// </summary>
	LightContent = 1,
	/// <summary>
	/// Dark style value
	/// </summary>
	DarkContent = 2
}