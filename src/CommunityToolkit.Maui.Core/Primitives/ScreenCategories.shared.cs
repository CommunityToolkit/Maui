{
	/// <summary>
	/// Extra Small sized screens
	/// </summary>
	ExtraSmall = 1,
	/// <summary>
	/// Small sized screens
	/// </summary>
	Small = 2,
	/// <summary>
	/// Medium size screens
	/// </summary>
	Medium = 3,
	/// <summary>
	/// Large sized screens
	/// </summary>
	Large = 4,
	/// <summary>
	/// Extra Large sized screens
	/// </summary>
	ExtraLarge = 5,

	/// <summary>
	/// Undefined/Unkown category
	/// </summary>
	NotSet = 0,
}